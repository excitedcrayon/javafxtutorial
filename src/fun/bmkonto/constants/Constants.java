/**
 * The Constants.java class holds constant values to be re-used by other 
 * classes.
 */
package fun.bmkonto.constants;
 
public abstract class Constants {
    
    // width constants
    /**
     * SMALL_WIDTH returns a width of 640px
     */
    public static final int SMALL_WIDTH = 640;
    /**
     * MEDIUM_WIDTH returns a width of 720px
     */
    public static final int MEDIUM_WIDTH = 1280;
    /**
     * LARGE_WIDTH returns a width of 1920px
     */
    public static final int LARGE_WIDTH = 1920;
    
    // height constants
    /**
     * SMALL_HEIGHT returns a height of 480px
     */
    public static final int SMALL_HEIGHT = 480;
    /**
     * MEDIUM_HEIGHT returns a height of 720px
     */
    public static final int MEDIUM_HEIGHT = 720;
    /**
     * LARGE_HEIGHT returns a height of 1080px
     */
    public static final int LARGE_HEIGHT = 1080;
}
